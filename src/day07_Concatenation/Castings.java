package day07_Concatenation;

public class Castings {

    public static void main(String[] args) {

        float averageNumber = 20.5f;

        byte byt = (byte) averageNumber; // Explicit casting
        short sh = (short) averageNumber; // Explicit casting
        int i = (int) averageNumber; // Explicit casting
        long l = (long) averageNumber; // Explicit casting
        float f = averageNumber; // no casting
        double d = averageNumber; // Inplicit casting
    }

}
